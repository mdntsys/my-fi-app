// Group derivation. Income + transfers-in are "Income"; everything else is
// "Expenses". Transfers-out are noise but we keep them in Expenses for
// completeness; the user can simply leave them unbudgeted.
export function groupOf(primary: PfcPrimary): PfcGroup {
  if (primary === "INCOME" || primary === "TRANSFER_IN") return "INCOME";
  return "EXPENSES";
}


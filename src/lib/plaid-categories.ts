// Convert a Plaid detailed code into a human label by stripping its primary
// prefix and Title-Casing the remainder. "FOOD_AND_DRINK_GROCERIES" → "Groceries".
// "FOOD_AND_DRINK_OTHER_FOOD_AND_DRINK" → "Other".
export function detailedName(detailed: string): string {
  const primary = PFC_DETAILED[detailed];
  if (!primary) return titleCaseFromSnake(detailed);
  let leaf = detailed.slice(primary.length + 1);
  // Collapse trailing "OTHER_<primary>" to just "Other".
  const otherSuffix = `OTHER_${primary}`;
  if (leaf === otherSuffix) leaf = "OTHER";
  return titleCaseFromSnake(leaf);
}

function titleCaseFromSnake(s: string): string {
  return s
    .toLowerCase()
    .split("_")
    .map((w) => (w === "and" ? "&" : w.charAt(0).toUpperCase() + w.slice(1)))
    .join(" ");
}

// All detailed codes that belong to a given primary, sorted alphabetically by
// friendly name for stable display order.
export function detailedFor(primary: PfcPrimary): string[] {
  return Object.entries(PFC_DETAILED)
    .filter(([, p]) => p === primary)
    .map(([d]) => d)
    .sort((a, b) => detailedName(a).localeCompare(detailedName(b)));
}

// All primary codes for a given group, in display order.
export function primariesFor(group: PfcGroup): PfcPrimary[] {
  return PLAID_PFC_PRIMARY.filter((p) => groupOf(p) === group);
}
